package model.vo;

public class RoupaVenda {

    private int idLocacao;
    private int idRoupa;
    private int tipoDeVenda;

    public RoupaVenda() {
    }

    public int getIdRoupa() {
        return idRoupa;
    }

    public void setIdRoupa(int idRoupa) {
        this.idRoupa = idRoupa;
    }

    public int getidLocacao() {
        return idLocacao;
    }

    public void setidLocacao(int idLocacao) {
        this.idLocacao = idLocacao;
    }

    public int getTipoDeVenda() {
        return tipoDeVenda;
    }

    public void setTipoDeVenda(int tipoDeVenda) {
        this.tipoDeVenda = tipoDeVenda;
    }


}
